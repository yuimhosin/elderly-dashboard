def _parse_区位() -> dict:
    """解析区位文本为 园区名 -> 城市名。"""
    out = {}
    for line in 区位文本.strip().splitlines():
        line = line.strip()
        if "-" not in line:
            continue
        city, park = line.split("-", 1)
        city, park = city.strip(), park.strip()
        out[park] = city
    return out


